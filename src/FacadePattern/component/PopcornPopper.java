package FacadePattern.component;

/**
 * @author lkmc2
 * @date 2018/9/2
 * @description 爆米花机
 */
public class PopcornPopper {

    public void on() {
        System.out.println("打开爆米花机");
    }

    public void off() {
        System.out.println("关闭爆米花机");
    }

    public void pop() {
        System.out.println("弹出爆米花");
    }
}
